STUDENT_NO, STUDENT_NAME
FROM TB_STUDENT
WHERE SUBSTR(ENTRANCE_DATE,1,2) < 100 AND SUBSTR(ENTRANCE_DATE,1,2) > 23
ORDER BY STUDENT_NO DESC;


SELECT 
STUDENT_NO, STUDENT_NAME, ENTRANCE_DATE
FROM TB_STUDENT
NATURAL JOIN TB_CLASS
WHERE DEPARTMENT_NO = '002';

--2
SELECT
PROFESSOR_NAME, PROFESSOR_SSN
FROM TB_PROFESSOR
WHERE PROFESSOR_NAME NOT LIKE '___';

--3
SELECT
PROFESSOR_NAME "교수이름",
CASE
WHEN TO_CHAR(SYSDATE, 'MMDD') >= SUBSTR(PROFESSOR_SSN, 3, 4)
THEN TO_CHAR(SYSDATE, 'YY')+100 - SUBSTR(PROFESSOR_SSN, 1, 2)  + 1
WHEN TO_CHAR(SYSDATE, 'DDMM') < SUBSTR(PROFESSOR_SSN, 3, 4)
THEN TO_CHAR(SYSDATE, 'YY')+100 - SUBSTR(PROFESSOR_SSN, 1, 2)
END "나이"
FROM TB_PROFESSOR
ORDER BY 2;

--4
